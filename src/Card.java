
public class Card 
{
    int id;
    String name;
    String type;
    int hp;
    int atk;
    int cost;
    
    public Card(int id, String name, String type, int hp, int atk, int cost)
    {
        this.id = id;
        this.name = name;
        this.type = type;
        this.hp = hp;
        this.atk = atk;
        this.cost = cost;
    }
}

